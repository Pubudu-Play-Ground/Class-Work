public class Demo4 {
    public static void main(String[] args) {
        // floating points

        //decimal
        System.out.println(10.0f);
        System.out.println(10.2e2f);
        System.out.println(10.2E2f);  // 10.2 * 10^2

        //hexadecimal
        System.out.println(0X1F.P2F);
        System.out.println(0XFF.P2F);
        System.out.println(0XA.AP1F);
        System.out.println(0XA.AP2F);   // (10.0+10/16)*2^2
        System.out.println(0X11.P1F);   // (16.0+1.0)*2^1

    }
}
